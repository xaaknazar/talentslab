<?php

namespace App\Services;

use Illuminate\Support\Facades\Http;
use Illuminate\Support\Facades\Log;

class OpenAIService
{
    private string $apiKey;
    private string $model;
    private string $baseUrl;

    public function __construct()
    {
        $this->apiKey = config('services.openai.api_key', '');
        $this->model = config('services.openai.model', 'gpt-4o-mini');
        $this->baseUrl = 'https://api.openai.com/v1';
    }

    /**
     * Анализирует резюме и формирует аналитический отчет
     */
    public function analyzeResume(string $resumeText, ?string $userComment = null): array
    {
        if (empty($this->apiKey)) {
            return [
                'success' => false,
                'error' => 'API ключ OpenAI не настроен. Добавьте OPENAI_API_KEY в .env файл.',
            ];
        }

        $systemPrompt = $this->getSystemPrompt();
        $userPrompt = $this->buildUserPrompt($resumeText, $userComment);

        try {
            $response = Http::timeout(180)
                ->withHeaders([
                    'Content-Type' => 'application/json',
                    'Authorization' => 'Bearer ' . $this->apiKey,
                ])
                ->post("{$this->baseUrl}/chat/completions", [
                    'model' => $this->model,
                    'messages' => [
                        [
                            'role' => 'system',
                            'content' => $systemPrompt,
                        ],
                        [
                            'role' => 'user',
                            'content' => $userPrompt,
                        ],
                    ],
                    'temperature' => 0.7,
                    'max_tokens' => 12000,
                ]);

            if ($response->successful()) {
                $data = $response->json();
                $text = $data['choices'][0]['message']['content'] ?? null;

                if ($text) {
                    return [
                        'success' => true,
                        'report' => $text,
                    ];
                }

                return [
                    'success' => false,
                    'error' => 'Не удалось получить ответ от модели',
                ];
            }

            $errorMessage = $response->json()['error']['message'] ?? 'Неизвестная ошибка API';
            Log::error('OpenAI API Error', [
                'status' => $response->status(),
                'body' => $response->body(),
            ]);

            return [
                'success' => false,
                'error' => "Ошибка API: {$errorMessage}",
            ];
        } catch (\Exception $e) {
            Log::error('OpenAI Service Exception', [
                'message' => $e->getMessage(),
                'trace' => $e->getTraceAsString(),
            ]);

            return [
                'success' => false,
                'error' => 'Ошибка при обращении к API: ' . $e->getMessage(),
            ];
        }
    }

    /**
     * Системный промпт для анализа резюме
     */
    private function getSystemPrompt(): string
    {
        return <<<'PROMPT'
Ты — Senior HR-аналитик и эксперт по организационному развитию. Тебе предоставлен PDF-файл, содержащий два типа данных:

Твоя основная цель — проанализировать сильные и слабые стороны профиля пользователя, выраженные в процентах (например: Творчество — 72%), и по каждой ключевой характеристике последовательно давать факты, аргументы, выводы и объяснения.

Для каждой стороны:
– зафиксируй уровень выраженности в процентах
– опиши, как именно эта черта проявляется в поведении и работе
– приведи логические доводы и наблюдаемые паттерны, подтверждающие этот уровень
– объясни, почему показатель высокий или низкий, без привязки к биографии
– сделай практический вывод: в чём это усиливает человека и где создаёт ограничения

Фокус держи на прикладной интерпретации: как данная сильная или слабая сторона влияет на стиль мышления, принятие решений, работу в команде и профессиональную реализацию.

База данных пользователя: Личные данные (возраст, семейное положение, образование, GPA, опыт работы, бэкграунд родителей).

Психометрический профиль: Результаты тестов (MBTI, Гарднер, Психотипы, Стили менеджмента, Метапрограммы и др.).

Твоя задача: Подготовить консолидированный аналитический отчет. Ты должен разобрать каждый пункт отчета и выделять сильные стороны и слабые стороны в %.

Требования к структуре отчета:

## Раздел 1. Социально-биографический аудит и контекст развития

Проанализируй данные из «базы данных»: возраст, текущий статус (студент/выпускник), образование.
Сформулируй гипотезу: как специальность в дипломе и опыт работы соотносятся с его реальными талантами. Укажи на возможные конфликты (например, «техническое образование при гуманитарном складе ума»).

## Раздел 2. Профессиональные компетенции и функциональный потенциал

Опиши ключевые зоны эффективности (HR, продажи, творчество, аналитика).
Используй проценты. Описывай через «силу функции»: в чем человек будет надежен, а где станет «слабым звеном».

Сопоставь уровни мышления (стратегическое/операционное) с его текущим опытом.

## Раздел 3. Управленческий профиль и лидерская зрелость

Оцени потенциал к руководству: экспертный путь или менеджерский.
Проанализируй стили менеджмента и «Радикальную прямоту».
Выяви барьеры в делегировании, опираясь на психотипы и эмпатию. Объясни, как возраст и малый стаж влияют на его способность управлять другими.

## Раздел 4. Интегративный психопрофиль (Синтез данных)

Объедини MBTI, типы интеллекта по Гарднеру и метапрограммы в единый портрет.
Опиши «фильтры восприятия» человека: как он принимает решения, на что опирается (внутренняя или внешняя референция) и как реагирует на стресс.

## Раздел 5. Итоговое заключение и дорожная карта

**Резюме:** для каких ролей и задач этот человек — идеальный кандидат прямо сейчас.
**Риски:** какие поведенческие особенности могут привести к ошибкам.
**Рекомендации:** конкретные векторы развития (обучение, смена профиля или коррекция стиля работы).

---

Правила оформления:
- Строгий деловой стиль. Никаких эмодзи и «инфоцыганства».
- Каждое утверждение должно быть обосновано данными (например: «Учитывая низкий показатель критического мышления (45%) в сочетании с высокой эмпатией, сотрудник склонен доверять на слово, что создает риски в...»).
- Текст должен выглядеть как готовый документ для передачи Генеральному директору или HR-департаменту.
PROMPT;
    }

    /**
     * Формирует пользовательский промпт
     */
    private function buildUserPrompt(string $resumeText, ?string $userComment = null): string
    {
        $prompt = "";

        // Контекст идёт первым и влияет на весь анализ
        if (!empty($userComment)) {
            $prompt .= "⚡ ВАЖНЫЙ КОНТЕКСТ ДЛЯ АНАЛИЗА:\n";
            $prompt .= "При формировании отчёта обязательно учитывай следующий контекст:\n";
            $prompt .= "---\n{$userComment}\n---\n\n";
            $prompt .= "Этот контекст должен влиять на:\n";
            $prompt .= "- Оценку соответствия кандидата указанной вакансии/роли (если указана)\n";
            $prompt .= "- Фокус анализа на релевантных для контекста компетенциях\n";
            $prompt .= "- Рекомендации с учётом специфики запроса\n";
            $prompt .= "- Выводы о пригодности кандидата для указанных задач\n\n";
            $prompt .= "================================\n\n";
        }

        $prompt .= "ДАННЫЕ ИЗ PDF-ДОКУМЕНТА:\n\n{$resumeText}";
        $prompt .= "\n\n---\n\nСформируй полный глубинный анализ личности согласно структуре. Обязательно используй все проценты и данные из документа.";

        if (!empty($userComment)) {
            $prompt .= " Особое внимание удели контексту, указанному в начале запроса.";
        }

        return $prompt;
    }

    /**
     * Извлекает текст из PDF файла
     */
    public function extractTextFromPdf(string $pdfPath): ?string
    {
        try {
            $parser = new \Smalot\PdfParser\Parser();
            $pdf = $parser->parseFile($pdfPath);
            $text = $pdf->getText();

            // Очистка текста от лишних пробелов и переносов
            $text = preg_replace('/\s+/', ' ', $text);
            $text = preg_replace('/\n{3,}/', "\n\n", $text);

            return trim($text);
        } catch (\Exception $e) {
            Log::error('PDF Parse Error', [
                'path' => $pdfPath,
                'message' => $e->getMessage(),
            ]);

            return null;
        }
    }
}
